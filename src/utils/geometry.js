/**
 * Get sin of angle
 *
 * @param {number} angle angle
 * @return {number} sin of angle
 */
export function sin(angle) {
    return Math.sin(Math.PI * angle / 180);
}

/**
 * Get cos of angle
 *
 * @param {number} angle angle
 * @return {number} cos of angle
 */
export function cos(angle) {
    return Math.cos(Math.PI * angle / 180);
}
